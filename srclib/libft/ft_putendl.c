#include "libft.h"

int		ft_putendl(char const *s)
{
	int		i;

	i = 0;
	if (s)
	{
		while (*s)
		{
			i += ft_putchar(*s);
			s++;
		}
		i += ft_putchar('\n');
	}
	return (i);
}
